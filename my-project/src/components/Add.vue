<template>
  <div class="add container">
  	<router-link to="/" class="btn btn-default">返回</router-link>
  	<Alert v-if="alert" :message="alert"></Alert>
 	<h1 class="page-header">添加用户</h1>
	 	<form v-on:submit="addCustomers">
	 		<div class="well">
	 			<h4>用户信息</h4>
	 			<div class="form-group">
	 			<label>姓名</label>
	 			<input type="text" class="form-control" placeholder="name" v-model="customer.name">
	 			</div>
				<div class="form-group">
	 			<label>电话</label>
	 			<input type="text" class="form-control" placeholder="phone" v-model="customer.phone">
	 			</div> 			
				<div class="form-group">
	 			<label>邮箱</label>
	 			<input type="text" class="form-control" placeholder="email" v-model="customer.email">
	 			</div> 			
				<div class="form-group">
	 			<label>学历</label>
	 			<input type="text" class="form-control" placeholder="education" v-model="customer.education">
	 			</div> 			
				<div class="form-group">
	 			<label>毕业学校</label>
	 			<input type="text" class="form-control" placeholder="graduateschool" v-model="customer.graduateschool">
	 			</div> 			
				<div class="form-group">
	 			<label>职业</label>
	 			<input type="text" class="form-control" placeholder="profession" v-model="customer.profession">
	 			</div> 			
				<div class="form-group">
	 			<label>个人简介</label>
	 			<textarea class="form-control" rows="10" v-model="customer.profile"></textarea>
	 			</div>
	 			<button class="btn btn-primary" type="submit">添加</button>	 			
 			</div>
 		</form>		
  	</div>
</template>
<script>
import Alert from './alert.vue'
export default {
  name: 'add',
  data () {
    return {
    	alert : "",
      	customer:{
      	}
    }
  },
  methods : {
  	addCustomers(e){
  		if(!this.customer.name || !this.customer.phone || !this.customer.email){
		this.alert = '请添加对应的信息'
  		}else{
  			let newCustomer = {
  				name : this.customer.name,
  				phone : this.customer.phone,
  				email : this.customer.email,
  				education : this.customer.education,
  				graduateschool : this.customer.graduateschool,
  				profession : this.customer.profession,
  				profile : this.customer.profile
  			}
  			this.axios.post('http://localhost:3000/users',newCustomer)
  			.then((response)=>{
  				this.$router.push({path:'/',query:{alert:'用户信息添加成功'}})
  				console.log(123)
  			})
  		}
  		e.preventDefault()
  	}
  },
  components : {
  	Alert
  }
}
</script>
